package cli

import (
	"context"

	"github.com/cosmos/cosmos-sdk/client"
	"github.com/cosmos/cosmos-sdk/client/flags"
	"github.com/spf13/cobra"

	"github.com/UnUniFi/chain/x/yieldaggregator/types"
)

func CmdQueryIntermediaryAccounts() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "intermediary-accounts",
		Short: "list intermediary accounts infos",
		RunE: func(cmd *cobra.Command, args []string) error {
			clientCtx := client.GetClientContextFromCmd(cmd)

			queryClient := types.NewQueryClient(clientCtx)

			params := &types.QueryIntermediaryAccountInfoRequest{}

			res, err := queryClient.IntermediaryAccountInfo(context.Background(), params)
			if err != nil {
				return err
			}

			return clientCtx.PrintProto(res)
		},
	}

	flags.AddQueryFlagsToCmd(cmd)

	return cmd
}
